class TestA7ReportsAreJSONSerializable:
    def setup_method(self) -> None:
        self.build_result = _build_scenario()
        self.residual_set = _make_residual_set()

    def test_three_layer_reports_serialize_together(self) -> None:
        req = ConfigurableResidualSelectionRequest(
            build_result=self.build_result,
            mode=ConfigurableResidualMode.CONFIGURABLE_ALGEBRAIC,
            algebraic_residual_set=self.residual_set,
            algebraic_unknown_values=_ZERO_UV,
            evaluate=True,
        )
        sel_result = select_configurable_residual_strategy(req)

        scenario_report = build_configurable_scenario_report(self.build_result)
        selection_report = build_configurable_residual_selection_report(sel_result)
        alg_report = build_configurable_algebraic_residual_report(sel_result.evaluation_result)

        combined = {
            "scenario": scenario_report,
            "selection": selection_report,
            "algebraic": alg_report,
        }
        serialized = json.dumps(combined)
        loaded = json.loads(serialized)

        assert loaded["selection"]["no_solve"] is True
        assert loaded["selection"]["selected_mode"] == "configurable_algebraic"
        assert loaded["algebraic"]["no_solve"] is True


