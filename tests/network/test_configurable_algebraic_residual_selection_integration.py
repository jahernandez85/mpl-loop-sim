class TestA3RoleChangesDoNotAffectBehavior:
    def setup_method(self) -> None:
        self.residual_set = _make_residual_set()

    def _check_eval(self, build_result: ConfigurableScenarioBuildResult) -> None:
        req = ConfigurableResidualSelectionRequest(
            build_result=build_result,
            mode=ConfigurableResidualMode.CONFIGURABLE_ALGEBRAIC,
            algebraic_residual_set=self.residual_set,
            algebraic_unknown_values=_ZERO_UV,
            evaluate=True,
        )
        result = select_configurable_residual_strategy(req)
        assert result.compatibility.is_compatible is True
        assert result.evaluation_performed is True
        for val in result.evaluation_result.residual_values.values():
            assert abs(val) < 1e-9

    def test_default_roles_evaluate_correctly(self) -> None:
        br = _build_scenario()
        self._check_eval(br)

    def test_all_generic_roles_evaluate_identically(self) -> None:
        br = _build_scenario(
            pump_role=ScenarioComponentRole.GENERIC,
            evap_role=ScenarioComponentRole.GENERIC,
            cond_role=ScenarioComponentRole.GENERIC,
        )
        self._check_eval(br)

    def test_mixed_roles_evaluate_identically(self) -> None:
        br = _build_scenario(
            pump_role=ScenarioComponentRole.ACCUMULATOR,
            evap_role=ScenarioComponentRole.PUMP,
            cond_role=ScenarioComponentRole.GENERIC,
        )
        self._check_eval(br)

    def test_roles_do_not_change_unknown_names(self) -> None:
        br_default = _build_scenario()
        br_generic = _build_scenario(
            pump_role=ScenarioComponentRole.GENERIC,
            evap_role=ScenarioComponentRole.GENERIC,
            cond_role=ScenarioComponentRole.GENERIC,
        )
        assert set(br_default.unknown_names) == set(br_generic.unknown_names)


