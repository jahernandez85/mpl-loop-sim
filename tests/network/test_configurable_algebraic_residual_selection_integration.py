"""Integration tests for Block 15F-B — Configurable Algebraic Residual Selection.

Proves that the CONFIGURABLE_ALGEBRAIC residual-selection mode integrates
correctly with the 15F-A algebraic residual foundation and the 15E-B
residual-selection layer.

Acceptance stories
------------------
A1  Build a configurable single-loop scenario, declare algebraic residuals,
    request CONFIGURABLE_ALGEBRAIC — evaluate at zero-residual point.
A2  Perturb one unknown — residual becomes nonzero.
A3  Change component roles while keeping structure and explicit residual
    declarations — selection/evaluation behavior does not change.
A4  Missing scenario unknown produces incompatible selection and no evaluation.
A5  DECLARATION_ONLY mode continues to work alongside the new mode.
A6  CLOSURE_ONLY mode continues to work alongside the new mode.
A7  Reports from all three stack layers are JSON-serializable together.
A8  Evaluate using evaluate_selected_configurable_residuals (requires params).
A9  Pure selection with evaluate=False does not perform evaluation.
A10 Extra unknown values are silently ignored (15F-A documented behavior).
A11 Report includes residuals_inferred_from_roles=False and
    residuals_inferred_from_topology=False.

Validation stories
------------------
V1  CONFIGURABLE_ALGEBRAIC rejects missing algebraic_residual_set.
V2  CONFIGURABLE_ALGEBRAIC accepts a valid explicit algebraic residual set.
V3  CONFIGURABLE_ALGEBRAIC rejects incompatible set with missing scenario unknowns.
V4  Evaluation requires evaluate=True.
V5  Evaluation requires algebraic_unknown_values.
V6  result.no_solve is always True.
V7  Report is JSON-serializable.
V8  Report includes required unknown names and no-inference flags.

Regression stories
------------------
R1  Production contracts still show NO_CONTRIBUTE_METHOD for all 6 classes.
R2  inspect_known_production_component_contracts returns exactly 6 results.

Boundary stories (module-attribute checks)
------------------------------------------
B1  configurable_residual_selection imports no CoolProp or PropertyBackend.
B2  No contribute attribute in the selection module.
B3  No SystemState or FluidState in the selection module.
B4  No solve(network) or NetworkGraph.solve() in the selection module.

These tests do NOT:
  - Call any solver or root-finder.
  - Execute production component physics.
  - Call CoolProp, PropertyBackend, or correlations.
  - Assemble SystemState or construct FluidState.
  - Write files, use pandas, or use numpy.
  - Infer residuals from roles or topology.
"""

